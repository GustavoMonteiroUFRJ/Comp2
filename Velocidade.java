
public class  Velocidade {
	private double vx;
	private double vy;
	
	public Velocidade(double vx, double vy) {
		this.setVx(vx);
		this.setVy(vy);
	}

	public double getVx() {
		return vx;
	}
	public double getVy() {
		return vy;
	}

	public void setVx(double vx) {
		this.vx = vx;
	}
	public void setVy(double vy) {
		this.vy = vy;
	}
	
}
