
public class Distancia {
	private double dx;
	private double dy;
	
	public Distancia(double dx, double dy) {
		this.setDx(dx);
		this.setDy(dy);
	}
	public Distancia(){ }

	public double getDx() {
		return dx;
	}
	public double getDy() {
		return dy;
	}

	public void setDx(double dx) {
		this.dx = dx;
	}
	public void setDy(double dy) {
		this.dy = dy;
	}

}
